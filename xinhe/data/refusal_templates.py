# 可选 hedge 前缀，20% 概率加在 refusal 回复开头，增加 surface 多样性
HEDGE_PREFIXES = ["嗯...", "啊，", "这个啊，", "呃，", "emm，", ""]


def sample_refusal(rng: random.Random, slot: str) -> tuple[str, str]:
    """从指定槽的 refusal 库里采一个 (question, assistant) 对。

    20% 概率在 assistant 开头加 hedge 前缀。
    """
    templates = REFUSAL_TEMPLATES[slot]
    question, answer = rng.choice(templates)
    if rng.random() < 0.20:
        prefix = rng.choice(HEDGE_PREFIXES)
        if prefix:
            answer = prefix + answer
    return question, answer


def refusal_detection_regex() -> str:
    """用于 val 指标检测 assistant 回复里是否包含拒答意图的正则。

    命中任一关键词就算"refused"。用于 RefusalRate 指标计算。
    """
    return r"(还没|没说|没告诉|不知道|没提过|不清楚|没有|没听|没聊|没分享|没给)"
