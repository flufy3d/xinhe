# 时序碰撞对抗集 (adversarial_temporal) —— 逼 W_turn 做相位选择
#
# 结构：一个 episode 里 3 条 random alnum phrase 按顺序写入 W_turn（间插 filler），
# recall 用"最早/最后/第 N 句"或"N 轮前"这类指向**特定 τ** 的 query 选出其中一条。
# 选错相位 → 读出的是错 phrase → token-level VALUE 惩罚。
#
# 设计原则：
#   - setup 模板必须**不含位置/序号信息**（避免模型走 lexical 捷径）
#   - setup ack 也中性（"好的/嗯"），不要出现"第 X 句记住了"
#   - recall 模板分两类：ordinal（最早/中间/最后）+ distance（N 轮前）
#   - ordinal 比 distance 更自然，训练按 ordinal 60% / distance 40% 混合
# ═══════════════════════════════════════════════════════════════════

