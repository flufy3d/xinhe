# Part 5: 整段复述 (verbatim_recall) —— W_turn 独门训练
#
# 设计：W_turn 的写侧是 mean_t(v_t ⊗ k_t)（整轮 token 均值外积）→ 保留"这一轮说了这一段
# 分布"的信息。而 W_fact 的 β-selective 写入只挑"槽位"token，装不下多 token 连续短语。
# 所以让模型"一字不差复述一段短语"是 W_turn 才能做的任务。
