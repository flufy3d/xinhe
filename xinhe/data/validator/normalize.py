"""无损折叠：去标点 / 全半角 / 简繁 / 大小写 / 空格。

只做"语义保留"折叠 —— 永远不动数字、字母组合、关键词的内部结构。
A17 ↔ A71 不会被折叠为相等；蓝色 ↔ 绿色 不会被折叠为相等。
"""
from __future__ import annotations

import unicodedata
import re


# 中文 + 英文常见标点
_PUNCT_RE = re.compile(
    r"[\s　\.\,\!\?\:\;\(\)\[\]\{\}\<\>\"\'`~@#\$%\^&\*\-_\+=\|/\\"
    r"、。！，：；？（）“”‘’《》【】]+"
)


# 简繁映射（最小集，覆盖训练数据中常见汉字）
# 完整 OpenCC 太重，这里硬编码常见对照即可。后续如有 mismatch 再扩展。
_SIMP_TRAD = str.maketrans({
    "後": "后", "為": "为", "這": "这", "那": "那", "說": "说", "話": "话",
    "東": "东", "車": "车", "馬": "马", "鳥": "鸟", "魚": "鱼", "貓": "猫",
    "狗": "狗", "雞": "鸡", "鴨": "鸭", "讓": "让", "見": "见", "幾": "几",
    "點": "点", "兒": "儿", "頭": "头", "麵": "面", "糖": "糖", "鹽": "盐",
    "醬": "酱", "醋": "醋", "雞": "鸡", "鴨": "鸭", "魚": "鱼", "蝦": "虾",
    "蟹": "蟹", "貝": "贝", "蛋": "蛋", "麥": "麦", "稻": "稻", "穀": "谷",
    "歲": "岁", "時": "时", "間": "间", "現": "现", "場": "场", "鄉": "乡",
    "農": "农", "業": "业", "員": "员", "組": "组", "織": "织", "團": "团",
    "體": "体", "個": "个", "張": "张", "雙": "双", "對": "对", "錯": "错",
    "問": "问", "題": "题", "答": "答", "覆": "覆", "蓋": "盖", "擦": "擦",
    "塗": "涂", "寫": "写", "讀": "读", "記": "记", "憶": "忆", "醒": "醒",
    "覺": "觉", "夢": "梦", "想": "想", "戀": "恋", "愛": "爱",
    "驕": "骄", "傲": "傲", "賤": "贱", "貴": "贵", "錢": "钱", "幣": "币",
    "鋼": "钢", "鐵": "铁", "銀": "银", "鉛": "铅", "錫": "锡", "鋅": "锌",
    "貝": "贝", "鄰": "邻", "鄕": "乡", "鎮": "镇", "區": "区", "縣": "县",
    "種": "种", "類": "类", "顏": "颜", "綠": "绿", "藍": "蓝", "紅": "红",
})


def _fold_simp_trad(s: str) -> str:
    return s.translate(_SIMP_TRAD)


def _fold_fullwidth(s: str) -> str:
    """全角→半角（NFKC 包含此映射 + 兼容字符规整）。"""
    return unicodedata.normalize("NFKC", s)


def fold(
    s: str,
    *,
    drop_punct: bool = True,
    fullwidth: bool = True,
    simp_trad: bool = True,
    case: bool = True,
) -> str:
    """语义保留折叠。返回小写、去标点、半角、简化的字符串。

    用例:
      fold("Hello, World!")  → "helloworld"
      fold("ＡＢＣ-１２３")   → "abc-123"  （NFKC 后保留 -123 数字+字母）

    注意：drop_punct=True 会去掉 - / 等连接符。如果需要保留 ID 类（A17, K9Q-27）的结构，
    在 tier.classify_tier 里使用 drop_punct=False。
    """
    if not s:
        return s
    if fullwidth:
        s = _fold_fullwidth(s)
    if simp_trad:
        s = _fold_simp_trad(s)
    if case:
        s = s.lower()
    if drop_punct:
        s = _PUNCT_RE.sub("", s)
    return s


def fold_keep_id(s: str) -> str:
    """ID 类折叠：保留连接符、数字大小写不变化等。仅做全半角 + 简繁 + 空格剔除。"""
    if not s:
        return s
    s = _fold_fullwidth(s)
    s = _fold_simp_trad(s)
    # 剔除空白但保留 - _ /
    s = re.sub(r"[\s　]+", "", s)
    return s
